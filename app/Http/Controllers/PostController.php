    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts = Post::where('user_id', Auth:user()->id)
                ->orderBy('created_at', 'desc')
                ->paginate(5);



        return view('post.index',['list' => $posts]);
    }

